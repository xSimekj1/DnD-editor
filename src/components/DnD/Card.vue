<template>
  <div
      :id="id"
      class="card"
      :draggable="draggable"
      @dragstart="dragStart"
      @dragover.stop
  >
    <slot/>
  </div>
</template>

<script>
export default {
  name: 'Card',
  props: {
    id: {
      type: String,
      required: true,
    },
    draggable: {
      type: Boolean,
      required: true,
    },
  },
  methods: {
    dragStart: e => {
      const target = e.target;

      e.dataTransfer.setData('card_id', target.id);

      setTimeout(() => {
        target.style.display = 'block';
      }, 0);
    },
  },
};
</script>

